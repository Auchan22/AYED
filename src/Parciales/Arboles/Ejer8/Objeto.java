package Parciales.Arboles.Ejer8;

public class Objeto {
    private String letra;
    private int pos;

    public Objeto(String letra, int pos) {
        this.letra = letra;
        this.pos = pos;
    }

    public String getLetra() {
        return letra;
    }

    public int getPos() {
        return pos;
    }
}
