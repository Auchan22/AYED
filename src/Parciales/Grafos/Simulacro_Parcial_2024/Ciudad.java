package Parciales.Grafos.Simulacro_Parcial_2024;

public class Ciudad {
    private String nombre;
    private int dias;

    public Ciudad(String nombre, int dias) {
        this.nombre = nombre;
        this.dias = dias;
    }

    public String getNombre() {
        return nombre;
    }

    public int getDias() {
        return dias;
    }
}
